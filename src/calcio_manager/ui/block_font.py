def render_block(text: str) -> str:
    """Render a string using the 5-row block font."""
    rows: list[str] = ["", "", "", "", ""]
    for i, ch in enumerate(text.upper()):
        glyph = BLOCK_FONT.get(ch, BLOCK_FONT[" "])
        sep = " " if i > 0 else ""
        for r in range(5):
            rows[r] += sep + glyph[r]
    return "\n".join(rows)
